import {
  describe,
  test,
  expect,
  jest,
  beforeEach,
  afterEach,
} from "@jest/globals";
import mockFs from "mock-fs";
import { readFileSync } from "node:fs";
import { join } from "node:path";

const validConfig = {
  slug: "my-project",
  globalName: "MyProject",
  localizeVar: "MyProjectLoc",
  textDomain: "my-project",
  hookPrefix: "my-project",
  npmScope: "@my-org",
};
const minimal = {
  slug: "test",
  globalName: "Test",
  localizeVar: "TestLoc",
  textDomain: "test",
  hookPrefix: "test",
  npmScope: "@test",
};

beforeEach(() => {
  jest.resetModules();
});
afterEach(() => {
  mockFs.restore();
});

/**
 * Phase 20.9 / 20.10 — v3 config tolerance.
 *
 * plan.v3.md §0.1 rule 7: "Every new config field needs a default.
 * A missing field must never crash. Old projects without the field
 * must keep working." Phase 20 adds two new top-level fields to
 * project.config.json:
 *
 *   - features   (the validated feature set; written by
 *                syncFeaturesToConfig in 20.15)
 *   - distMode   (the distribution mode; "deps" by default (Phase 23)
 *                in Phase 20, flipped to "deps" by Phase 23)
 *
 * readProjectConfig() must:
 *  1. Accept a config that has these new fields without throwing
 *     (the file is from a project generated by a v3-aware kit).
 *  2. Accept a config that has UNKNOWN future fields without
 *     throwing (forward-compat — the consumer's project may
 *     include fields a later kit version added).
 *  3. Still return every v2 default for projects that omit them
 *     (BC — old v2 projects must keep working).
 *  4. Still validate v2 fields when they are present.
 *
 * The implementation choice is "ignore unknown keys silently
 * rather than error on them". The contract is locked here so a
 * future tightening of the validator (e.g. a "strict" mode) has
 * to update this test first.
 */
describe("readProjectConfig — v3 tolerance (Phase 20.9/20.10)", () => {
  test("accepts a config with a `features` key without throwing", async () => {
    const v3Config = {
      ...minimal,
      features: {
        js: "typescript",
        jsLib: "preact",
        jsTest: "jest",
        phpMinVersion: "7.4",
        phpFramework: "none",
        phpTest: "phpunit",
        restBatch: "on",
        faultTolerance: "off",
        vendorScoping: "on",
        husky: "on",
        css: "none",
        blocks: "off",
        license: "gpl2",
        wpMinVersion: "6.0",
        exampleFeature: "on",
        i18n: "on",
      },
    };
    mockFs({ "/tmp/v3.json": JSON.stringify(v3Config) });
    const { readProjectConfig } = await import("@core/utils");
    expect(() => readProjectConfig({ path: "/tmp/v3.json" })).not.toThrow();
    const result = readProjectConfig({ path: "/tmp/v3.json" });
    expect(result.features).toBeDefined();
    expect(result.features.js).toBe("typescript");
  });

  test("accepts a config with unknown future keys without throwing", async () => {
    const futureConfig = {
      ...minimal,
      // Pretend a future kit version added these.
      experimentalFlag: true,
      futurePluginApi: { endpoint: "/x/v1" },
      futureList: ["a", "b", "c"],
    };
    mockFs({ "/tmp/future.json": JSON.stringify(futureConfig) });
    const { readProjectConfig } = await import("@core/utils");
    expect(() => readProjectConfig({ path: "/tmp/future.json" })).not.toThrow();
    const result = readProjectConfig({ path: "/tmp/future.json" });
    // The future keys must round-trip through readProjectConfig
    // so consumers can rely on them being visible.
    expect(result.experimentalFlag).toBe(true);
    expect(result.futurePluginApi).toEqual({ endpoint: "/x/v1" });
    expect(result.futureList).toEqual(["a", "b", "c"]);
  });

  test("accepts a v3 config with both `features` AND unknown future keys", async () => {
    const v3plusConfig = {
      ...minimal,
      features: { js: "typescript", jsLib: "none", jsTest: "jest" },
      futureKey: "future-value",
    };
    mockFs({ "/tmp/v3plus.json": JSON.stringify(v3plusConfig) });
    const { readProjectConfig } = await import("@core/utils");
    const result = readProjectConfig({ path: "/tmp/v3plus.json" });
    expect(result.features).toBeDefined();
    expect(result.futureKey).toBe("future-value");
  });

  test("still returns every v2 default when omitted (BC)", async () => {
    // The v3 contract must NOT regress v2 defaults. An old v2
    // project (no `features`, no unknown keys) must still see
    // the full default set.
    mockFs({ "/tmp/v2-only.json": JSON.stringify(minimal) });
    const { readProjectConfig } = await import("@core/utils");
    const result = readProjectConfig({ path: "/tmp/v2-only.json" });
    expect(result.restNamespace).toBe("wpsk/v1");
    expect(result.vendorPrefix).toBe("WpskVendor");
    expect(result.phpMinVersion).toBe("7.4");
    expect(result.phpSourceVersion).toBe("8.1");
    expect(result.batchEndpoint).toBe("/batch/v1");
    expect(result.depsBundle).toBe("test-deps.js");
    expect(result.phpFunctionPrefix).toBe("wpsk_");
    expect(result.uiFramework).toBe("preact");
  });

  test("still validates v2 fields when present (BC — must not be relaxed)", async () => {
    // A v3-aware reader must NOT silently drop the v2 validation
    // when a `features` key is also present. The v2 contract
    // (bad vendorPrefix → throw) survives the v3 extension.
    const config = {
      ...minimal,
      vendorPrefix: "1Bad", // starts with a digit → invalid
      features: { js: "typescript" },
    };
    mockFs({ "/tmp/v3-bad-vendor.json": JSON.stringify(config) });
    const { readProjectConfig } = await import("@core/utils");
    expect(() =>
      readProjectConfig({ path: "/tmp/v3-bad-vendor.json" }),
    ).toThrow(/vendorPrefix/);
  });

  test("preserves v2 explicit values when `features` is also present", async () => {
    // The merged object must contain BOTH the explicit v2 values
    // and the v3 features object. The two namespaces are
    // independent: v2 fields describe the project's branding
    // (slug, scope, etc.); `features` describes the kit's
    // capability flags. They can co-exist on the same object.
    const config = {
      ...validConfig,
      restNamespace: "myns/v2",
      vendorPrefix: "AcmeVendor",
      features: { js: "typescript" },
    };
    mockFs({ "/tmp/v3-explicit.json": JSON.stringify(config) });
    const { readProjectConfig } = await import("@core/utils");
    const result = readProjectConfig({ path: "/tmp/v3-explicit.json" });
    expect(result.restNamespace).toBe("myns/v2");
    expect(result.vendorPrefix).toBe("AcmeVendor");
    expect(result.features).toBeDefined();
  });
});

/**
 * Source-level contract: the implementation must explicitly
 * merge unknown keys (not just accidentally do so via object
 * spread). This guards against a future "strict" validator
 * that would reject unknown keys — the v3 plan says we must
 * tolerate them for forward-compat.
 *
 * If a reviewer ever changes readProjectConfig to reject
 * unknown keys, this test forces them to revisit the v3
 * tolerance contract first.
 */
describe("readProjectConfig — v3 source contract", () => {
  test("the real source explicitly tolerates unknown keys (no strict whitelist)", () => {
    const source = readFileSync(
      join(process.cwd(), "core/packages/utils/readProjectConfig.js"),
      "utf8",
    );
    // The validator's v2 field loop is restricted to the known
    // v2 set — so anything outside that set is silently kept.
    // The implementation pattern is: merge defaults + config,
    // then validate a known-subset, then return. We assert the
    // shape of that pattern here so a future refactor that adds
    // a strict whitelist (e.g. `Object.keys(config).forEach`
    // with a known-only set) gets flagged.
    expect(source).toMatch(
      /merged\s*=\s*\{\s*\.\.\.defaults\s*,\s*\.\.\.config\s*\}/,
    );
    // The known v2 field list is hard-coded so an unknown key
    // doesn't get a per-field validation pass.
    const v2FieldLoop = source.match(
      /for\s*\(\s*const\s+field\s+of\s*\[([^\]]+)\]\s*\)/,
    );
    expect(v2FieldLoop).not.toBeNull();
    // The loop body must guard on `config[field] !== undefined`
    // — i.e. only validate v2 fields that are actually present.
    expect(source).toMatch(
      /config\[field\]\s*!==\s*undefined[\s\S]{0,120}validateV2/,
    );
  });
});
