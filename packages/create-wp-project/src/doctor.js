/**
 * Placeholder for the vendored framework checksum check.
 * The contract (per Phase 24.10) is:
 *  - The function exists and is invoked when the project
 *    is in `distMode: "vendored"`.
 *  - When the framework files are absent (the project
 *    hasn't scaffolded yet, or the vendored dir was
 *    deleted), the function is a no-op — `doctorProject`
 *    does not warn.
 *  - When the files ARE present, the function reads them
 *    and compares to a shipped manifest (Phase 25+). The
 *    placeholder returns `[]` (no warnings).
 *
 * Exported so the test can verify the wiring (the dispatch
 * in `doctorProject` calls THIS function), and so a future
 * phase can replace the body without touching the dispatch.
 *
 * @param {string} dir
 * @returns {string[]}  array of warning messages (empty = ok)
 */
export function checkVendoredChecksum(dir) {
  if (!dir || typeof dir !== "string") return [];
  const warnings = [];
  // Legacy vendored (pre-Phase 23): framework sources lived as
  // src/Core copies inside the project (not under the Composer
  // vendor/ path until after install + strauss). Presence of
  // these in a modern project is drift — recommend update.
  const legacyCore = path.join(dir, "src", "Core", "Plugin.php");
  if (existsSync(legacyCore)) {
    warnings.push(
      "Legacy vendored framework sources found under src/Core/. " +
        "Run `wpdev update` (or delete src/Core/ after confirming " +
        "your modules only use public WPDev APIs) to migrate to deps mode.",
    );
  }
  const frameworkDir = path.join(dir, VENDORED_FRAMEWORK_DIR);
  if (existsSync(frameworkDir)) {
    // Real installed copy under vendor (post composer). No checksum
    // validation in v3 baseline; the dep + composer.lock is source of
    // truth. Placeholder kept for forward compat.
    // (In a future version we could hash against a manifest.)
  }
  return warnings;
}

/* -------------------------------------------------------------------- */
/* doctorProject                                                          */
/* -------------------------------------------------------------------- */

/**
 * Run the doctor on a project rooted at `dir`. Returns
 * `{ok, warnings, errors}`. NEVER throws.
 *
 * @param {string} dir
 * @returns {{ ok: boolean, warnings: string[], errors: string[] }}
 */
export function doctorProject(dir) {
  const result = { ok: true, warnings: [], errors: [] };
  if (!dir || typeof dir !== "string") {
    result.errors.push("manifest missing");
    result.ok = false;
    return result;
  }

  // Check 1: manifest present.
  let manifest;
  try {
    manifest = readManifest(dir);
  } catch (error) {
    // Malformed manifest → warning (not error). The
    // installer's "broken manifest" UX is "show the user the
    // path + parser message, suggest `git checkout .` if
    // they didn't mean to edit it".
    result.warnings.push(
      `manifest unreadable: ${error && error.message ? error.message : String(error)}`,
    );
    return result;
  }
  if (!manifest) {
    result.errors.push("manifest missing");
    result.ok = false;
    return result;
  }

  // Check 1b: unsupported manifest schema.
  if (
    typeof manifest.schema === "number" &&
    manifest.schema !== MANIFEST_SCHEMA
  ) {
    result.errors.push(
      `unsupported manifest schema ${manifest.schema} — upgrade the kit before updating this project (expected schema ${MANIFEST_SCHEMA})`,
    );
  }

  // Check 2: unknown feature ids.
  const knownIds = new Set(getFeatureCatalog().map((f) => f.id));
  const features = (manifest && manifest.features) || {};
  for (const id of Object.keys(features)) {
    if (!knownIds.has(id)) {
      result.errors.push(`unknown feature: ${id}`);
    }
  }

  // Check 3: manifest.kitVersion vs installed kit's own version.
  // A manifest NEWER than the running kit is a warning. We
  // ALSO verify the dep registry is non-empty — if the
  // registry is empty (e.g. the kit's package.json was
  // missing), the check is a no-op (we can't compare).
  const installed = readInstalledKitVersion();
  const project =
    typeof manifest.kitVersion === "string" ? manifest.kitVersion : null;
  if (project && installed !== "0.0.0") {
    if (compareSemver(project, installed) > 0) {
      result.warnings.push(
        `project manifest kitVersion (${project}) is newer than the installed kit (${installed}) — run wpdev update`,
      );
    }
  }

  // Check 4: vendored framework checksum.
  if (manifest.distMode === "vendored") {
    const vendoredWarnings = checkVendoredChecksum(dir);
    for (const w of vendoredWarnings) {
      result.warnings.push(w);
    }
  }

  // The dep registry is consulted only to keep the dep-versions
  // module warm (caching the disk read). Reading it here also
  // surfaces a future "registry is empty" condition as a
  // warning if it ever fires — the kit's package.json
  // disappearance is an installation bug, not a user error.
  const registry = getDepVersions();
  if (registry.size === 0) {
    result.warnings.push(
      "dep registry is empty — the kit's own package.json/composer.json may be missing",
    );
  }

  result.ok = result.errors.length === 0;
  return result;
}
